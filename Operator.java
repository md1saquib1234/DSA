public class Operator {
    public static void main(String[] args) {
       int A = 10;
       int B = 5;
       System.out.println("Add = "+(A+B)); 
       System.out.println("Add = "+(A-B));
       System.out.println("Add = "+(A*B));
       System.out.println("Add = "+(A/B));
       System.out.println("Add = "+(A%B));
    }
}
